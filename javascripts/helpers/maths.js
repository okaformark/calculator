const add = (num1, num2) => {
    return num1*1 + num2*1;
};
const substract = (num1,num2) => {
    return num1*1 - num2*1;
};
const multiply = (num1, num2) => {
    return num1 * num2;
};
const divide = (num1, num2) => {
    return num1 / num2;
};

export default {add, substract, multiply, divide};